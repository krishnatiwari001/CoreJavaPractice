package JavaBasic;

public class DataType1 {

	public static void main(String[] args) {
		//Primitive data type: integer, double, char, boolean.
		//integer: we can store only non decimal values
		int a=10;
		a=20;
		int b=40;
		System.out.println(a);
		System.out.println(b);
		
		//double: we can store decimal values and non decimal values as decimal
		double d=10.20;
		double d1=20;
		System.out.println(d);
		System.out.println(d1);//20.0
		
		//char: we can store only characters using single quotes ('')
		char c = 'K';
		char c1 = 'R';
		System.out.println(c);
		System.out.println(c1);
		
		//boolean: we can use this for true and false conditions and same keyword also present in java
		boolean b1=true;
		boolean b2=false;
		System.out.println(b1);
		System.out.println(b2);
	}

}
