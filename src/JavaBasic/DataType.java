package JavaBasic;

public class DataType {

	public static void main(String[] args) {
		//Primitive: integer, char, double, boolean
		//Integer: we can store only integer values
		int a=10;
		a=20;
		System.out.println(a);//it will replace value of a by 20
		
		//Double: We can store decimal values and Integer values as Decimal also
		double d=12.33;
		double d1=100;
		System.out.println(d);
		System.out.println(d1);
		
		//Char: We can store only single character using single quote('')
		char c='K';
		char c1='1';
		System.out.println(c);
		System.out.println(c1);
		
		//Boolean: we can use for true and false only and same key word also present in java
		boolean b=true;
		boolean b1=false;
		System.out.println(b);
		System.out.println(b1);
		
		//String: We can store whole word or sentence using double quote("") and String is class not a datatype
		String S = "Krishna";
		String s1 = "100";
		System.out.println(S);
		System.out.println(s1);
	}

}
