package JavaBasic;

public class DataType3 {

	public static void main(String[] args) {
//Premitive Datatype: int,double,char,boolean
		//Integer: we can store only non decimal values
		int a=10;
		int b=90;
		b=100;
		System.out.println(a);//10;
		System.out.println(b);//100 
		
		//Double: we can store decimal values and integer values as decimal
		double d = 123.32;
		double d1=100;
		System.out.println(d);
		System.out.println(d1);
		
		//Char: we can store only char value
		char c='K';
		char c1='1';
		System.out.println(c);
		System.out.println(c1);
		
		//boolean: we can store only true and false values
		boolean b2=true;
		boolean b1=false;
		System.out.println(b2);
		System.out.println(b1);
		
		//String: String is a class not a datatype
		String s="Krishna";
		String s1="Tiwari";
		String s2="32";
		System.out.println(s);
		System.out.println(s1);
		System.out.println(s2);

	}

}
