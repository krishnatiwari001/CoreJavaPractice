package JavaBasic;

public class DataType2 {

	public static void main(String[] args) {
		//Primitive data type: integer,char,double,boolean
		//Integer: we can store only integer values
		int a=10;
		int b=20;
		int c=a+b;
		System.out.println(c);
		
		//Char: we can store only character value using single quotes('')
		char c1='K';
		char c2='R';
		char c3='I';
		System.out.println(c1);
		System.out.println(c2);
		System.out.println(c3);
		
		//Double: we can store decimal values as well as integer values as decimal
		double d=10.30;
		double d1=30;
		System.out.println(d);
		System.out.println(d1);
		
		//boolean: we can store only true false value and same keyword also present in java
		boolean b1=true;
		boolean b2=false;
		System.out.println(b1);
		System.out.println(b2);
		
		//String : we can store only Strings using double quotes("")
		String s = "Krishna";
		System.out.println(s);
	}

}
