package JavaBasic;

public class DataType_1 {

	public static void main(String[] args) {
		// Primitive data type: int, double,char, boolean
		
		//integer; we can store only non decimal values(integer values)
		int i=10;
		i=20;
		System.out.println(i);//output will be 20 because i value is replacing by 20
		int j=30;
		System.out.println(j);//30
		
		//double: we can store integer value as decimal and decimal vlues also
		double d=50.55;
		double d1=100;
		System.out.println(d);//50.55
		System.out.println(d1);//100.00
		
		//char: we can store char values using single quotes
		char c= 'K';
		char c1='R';
		char c2='I';
		System.out.println(c);
		System.out.println(c1);
		System.out.println(c2);
		
		//boolean: in boolean data type we can store only true false values but same keywords also present in java
		boolean b=true;
		boolean b1=false;
		System.out.println(b);
		System.out.println(b1);
		
		//String: String is a class not a data type and we can store full string
		String s="Krishna";
		String s1="100";//integer value stored as string
		System.out.println(s);
		System.out.println(s1);

	}

}
